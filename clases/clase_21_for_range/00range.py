

# Un argumento en range inicia siempre desde 0 y termina en ese argumento - 1
varRango = range(5) # 0, 1, 2, 3, 4 [0, 5)


# Dos argumentos inicia en el valor del primer arg y termina en el ultimo argumento - 1
varRango = range(2,5) #  2,  3, 4

# Con tres argumentos
varRango = range(2,10,2) # 2, 4, 6, 8, 

print(">>>>> ",varRango)